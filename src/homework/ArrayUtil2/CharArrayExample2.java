package homework.ArrayUtil2;

public class CharArrayExample2 {
    public static void main(String[] args) {
        char[] chars2 = {'j', 'a', 'v', 'a', 'l', 'o', 'v', 'e'};

        for (int i = chars2.length / 2 - 1; i <= chars2.length / 2; i++) {

            System.out.println(chars2[i] + " ");

        }


    }
}
